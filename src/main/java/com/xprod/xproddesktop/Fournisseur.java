    private void tableUpdate() {
        int cpt;// déclaration d'une variable que je nomme compteur

        try {
            
            con = cn.getCon();
            
            String query = "SELECT * FROM fournisseur";
            pst = con.prepareStatement(query);

            ResultSet rs = pst.executeQuery(); //Je mets mes données dans un tableau de résultat

            ResultSetMetaData rsmd = rs.getMetaData();// Je récupère toute mes métadonnées du tableau de résultat
            cpt = rsmd.getColumnCount();// Je compte toute mes métadonnées

            DefaultTableModel dtm = (DefaultTableModel) tableFournisseur.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {//Tant que j'ai des données dans mon tableau de résultat
                Vector vect = new Vector();
                vect.add(rs.getString("idFournisseur"));
                vect.add(rs.getString("nomFournisseur"));
                vect.add(rs.getString("rsFournisseur"));
                vect.add(rs.getString("rueFournisseur"));
                vect.add(rs.getString("cpFournisseur"));
                vect.add(rs.getString("villeFournisseur"));
                vect.add(rs.getString("paysFournisseur"));
                vect.add(rs.getString("telFournisseur"));
                vect.add(rs.getString("faxFournisseur"));
                vect.add(rs.getString("emailFournisseur"));
                dtm.addRow(vect);
            }

        } catch (Exception e) {
        }
    }

