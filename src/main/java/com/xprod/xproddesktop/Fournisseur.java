    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
        // Supprimer
        DefaultTableModel dtm = (DefaultTableModel) tableFournisseur.getModel();
        int SelectIndex = tableFournisseur.getSelectedRow();
        int IDFournisseur = Integer.parseInt(dtm.getValueAt(SelectIndex, 0).toString());
        
        int dialogResult = JOptionPane.showConfirmDialog(null, "Voulez-vous vraiment supprimer ce fournisseur ?", "ATTENTION !", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            
            try {
                
                con = cn.getCon();
                
                pst = con.prepareStatement("DELETE FROM fournisseur WHERE idFournisseur=?");
                pst.setInt(1, IDFournisseur);
                pst.executeUpdate();
                /*System.out.println("Je suis dans la suppresion");*/
                JOptionPane.showMessageDialog(this,"Données supprimées !");
                
                //Je vide le contenu des champs remplis
                txtNom.setText("");
                txtRS.setText("");
                txtRue.setText("");
                txtCodePostal.setText("");
                txtVille.setText("");
                txtPays.setText("");
                txtTel.setText("");
                txtFax.setText("");
                txtEmail.setText("");
                
                tableUpdate(); //Mettre à jour la table des fournisseurs
                
            } catch (SQLException ex) {
                Logger.getLogger(Fournisseur.class.getName()).log(Level.SEVERE, null, ex);
                
            }
        }
    }//GEN-LAST:event_btnSupprimerActionPerformed

